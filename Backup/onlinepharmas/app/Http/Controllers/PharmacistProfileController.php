    public function update(Request $request, $id)
    {
        $usr=User::find($id);
        $usr->name=$request->name;   //left side is from database and right side is from form
        $usr->address=$request->address;
        $usr->gender=$request->sex;
        $usr->phone_no=$request->phone_no;
        $usr->email=$request->email;

        $usr->save();
        return redirect()->to('/pharmacistPanel')->with('success','Profile updated successfully');
    }

